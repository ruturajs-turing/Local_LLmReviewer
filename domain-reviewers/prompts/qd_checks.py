def _format_env_contents(task: dict, max_total: int = 60_000) -> str:
    """Format environment file contents for inclusion in QD prompts (--msource)."""
    contents = task.get("env_file_contents", [])
    if not contents or not task.get("_msource"):
        return ""
    lines = ["\n--- ENVIRONMENT FILE CONTENTS (--msource deep review) ---"]
    total = 0
    for ef in contents:
        if total >= max_total:
            lines.append(f"(remaining files truncated at {max_total} chars)")
            break
        budget = max_total - total
        text = ef["content"]
        if len(text) > budget:
            text = text[:budget] + "\n... (truncated)"
        lines.append(f"\n--- environment/{ef['name']} ({ef['size']} bytes) ---")
        lines.append(text)
        total += len(text)
    return "\n".join(lines)


