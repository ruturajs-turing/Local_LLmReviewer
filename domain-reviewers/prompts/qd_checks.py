    dockerfile = task.get("dockerfile") or "(missing)"
    test_sh = task.get("test_sh") or "(missing)"
    task_toml = task.get("task_toml_raw") or "(missing)"
    folder_name = task.get("folder_name", "(unknown)")
    slug = task.get("slug", "(unknown)")

    env_files = task.get("env_files", [])
    env_formatted = "\n".join(f"- {f}" for f in env_files) if env_files else "(no environment files)"
    env_contents = _format_env_contents(task)

    if qd_id == "A":
        base = (
            f"--- instruction.md ---\n{instruction}\n\n"
            f"--- ENVIRONMENT FILES (in environment/ folder) ---\n{env_formatted}\n\n"
            f"--- Dockerfile ---\n{dockerfile}\n\n"
            f"--- solve.sh ---\n{solve_sh}"
        )
        if env_contents:
            base += (
                f"\n\n{env_contents}\n\n"
                "MSOURCE: Cross-check that data files above match what "
                "instruction.md describes (column names, schemas, values, formats)."
            )
        return base

    if qd_id == "B":
        base = (
            f"FILES:\n--- instruction.md ---\n{instruction}\n\n"
            f"--- tests/{task.get('test_filename', 'test_outputs.py')} ---\n{test_code}"
        )
        if env_contents:
            base += (
                f"\n\n{env_contents}\n\n"
                "MSOURCE: Verify that hardcoded expected values in tests are "
                "derivable from the environment data above. Flag any magic values "
                "that cannot be traced to these source files."
            )
        return base

    if qd_id == "C":
        return (
            f"FILES:\n--- root-folder-name ---\n{folder_name}\n\n"
            f"--- instruction.md ---\n{instruction}"
        )

    if qd_id == "E":
        base = (
            f"FILE: Dockerfile\n----------------\n{dockerfile}\n\n"
            f"Context (environment files):\n{env_formatted}\n\n"
            f"FILE: instruction.md\n{instruction}"
        )
        if env_contents:
            base += (
                f"\n\n{env_contents}\n\n"
                "MSOURCE: Verify that Dockerfile COPY commands cover all "
                "environment files needed by the task."
            )
        return base

    if qd_id == "F":
        test_filename = task.get("test_filename", "test_outputs.py")
        return (
            f"FILES:\n--- test.sh (actual) ---\n{test_sh}\n\n"
            f"--- test.sh (DEFAULT reference) ---\n{DEFAULT_TEST_SH}\n\n"
            f"--- tests directory files ---\n"
            f"- {test_filename}"
        )

    if qd_id == "G":
        return (
            f"FILES:\n--- instruction.md ---\n{instruction}\n\n"
            f"--- solve.sh ---\n{solve_sh}\n\n"
            f"--- Dockerfile content ---\n{dockerfile}"
        )

    if qd_id == "H":
        return f"TASK SLUG: {slug}\n\n--- task.toml ---\n{task_toml}"

    if qd_id == "J":
        return (
            f"FILES:\n--- tests/{task.get('test_filename', 'test_outputs.py')} ---\n{test_code}\n\n"
            f"--- test.sh ---\n{test_sh}"
        )

    if qd_id == "M":
        base = (
            f"FILES:\n--- instruction.md ---\n{instruction}\n\n"
            f"--- tests/{task.get('test_filename', 'test_outputs.py')} ---\n{test_code}"
        )
        if env_contents:
            base += (
                f"\n\n{env_contents}\n\n"
                "MSOURCE: With actual environment data available, perform deeper "
                "alignment checks:\n"
                "- Are test expected values consistent with the actual input data?\n"
                "- Do instruction claims about data properties (row count, columns, "
                "value ranges) match reality?\n"
                "- Are there hidden data dependencies that tests rely on but "
                "instruction.md doesn't mention?"
            )
        return base

    return f"(Unknown QD: {qd_id})"
